export type TranslationKey = keyof typeof dictionaries.en

function interpolate(template: string, params?: TranslationParams): string {
  if (!params) return template
  return template.replace(/\{([^}]+)\}/g, (match, key: string) => (
    Object.prototype.hasOwnProperty.call(params, key) ? String(params[key]) : match
  ))
}

export function translate(locale: PluginLocale, key: string, params?: TranslationParams): string {
  const localized = (dictionaries[locale] as Record<string, string>)[key]
  const fallback = (dictionaries.en as Record<string, string>)[key]
  return interpolate(localized ?? fallback ?? key, params)
}

export function initI18n(localeRef: LocaleRef) {
  return {
    t(key: TranslationKey | string, params?: TranslationParams): string {
      return translate(localeRef.value, key, params)
    },
    locale(): PluginLocale {
      return localeRef.value
    },
  }
}

export function normalizeLocaleSetting(value: unknown): LocaleSetting {
  return value === 'zh' || value === 'en' ? value : 'auto'
}

export function resolveLocale(setting: LocaleSetting, documentLanguage = ''): PluginLocale {
  if (setting === 'zh' || setting === 'en') return setting
  return documentLanguage.trim().toLowerCase().startsWith('zh') ? 'zh' : 'en'
}
